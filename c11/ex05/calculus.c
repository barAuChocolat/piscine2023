int	ft_add(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int	ft_substack(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int	ft_multiply(int nb1, int nb2)
{
	return (nb1 * nb2);
}

int	ft_divide(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int	ft_mod(int nb1, int nb2)
{
	return (nb1 / nb2);
}